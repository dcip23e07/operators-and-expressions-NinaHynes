# Convert 4999 to binary
number1 = 4999
binary_number1 = bin(number1)

# Convert 2111 to binary
number2 = 2111
binary_number2 = bin(number2)

# Perform bitwise AND
result_and = number1 & number2

# Perform bitwise OR
result_or = number1 | number2

print(f"Binary representation of 4999: {binary_number1}")
print(f"Binary representation of 2111: {binary_number2}")
print(f"Bitwise AND result: {result_and}")
print(f"Bitwise OR result: {result_or}")